package com.example.movieapp.service.pojo;

import android.widget.ImageView;

import androidx.databinding.BindingAdapter;

import com.example.movieapp.R;
import com.example.movieapp.utilities.Constant;
import com.squareup.picasso.Picasso;

public class Movie {

     private double popularity,vote_average;
     private int vote_count,id;
     private String poster_path,backdrop_path,original_language,original_title,title,overview,release_date;
     private boolean adult,video;
     private int [] genre_ids;

    public double getPopularity() {
        return popularity;
    }

    public void setPopularity(double popularity) {
        this.popularity = popularity;
    }

    public double getVote_average() {
        return vote_average;
    }

    public void setVote_average(double vote_average) {
        this.vote_average = vote_average;
    }

    public int getVote_count() {
        return vote_count;
    }

    public void setVote_count(int vote_count) {
        this.vote_count = vote_count;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getPoster_path() {
        return poster_path;
    }

    public void setPoster_path(String poster_path) {
        this.poster_path = poster_path;
    }

    public String getBackdrop_path() {
        return backdrop_path;
    }

    public void setBackdrop_path(String backdrop_path) {
        this.backdrop_path = backdrop_path;
    }

    public String getOriginal_language() {
        return original_language;
    }

    public void setOriginal_language(String original_language) {
        this.original_language = original_language;
    }

    public String getOriginal_title() {
        return original_title;
    }

    public void setOriginal_title(String original_title) {
        this.original_title = original_title;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getOverview() {
        return overview;
    }

    public void setOverview(String overview) {
        this.overview = overview;
    }

    public String getRelease_date() {
        return release_date;
    }

    public void setRelease_date(String release_date) {
        this.release_date = release_date;
    }

    public boolean isAdult() {
        return adult;
    }

    public void setAdult(boolean adult) {
        this.adult = adult;
    }

    public boolean isVideo() {
        return video;
    }

    public void setVideo(boolean video) {
        this.video = video;
    }

    public int[] getGenre_ids() {
        return genre_ids;
    }

    public void setGenre_ids(int[] genre_ids) {
        this.genre_ids = genre_ids;
    }



    // important code for loading image here
    @BindingAdapter({ "movieImage" })
    public static void loadImage(ImageView image, String imageURL) {
        if(!imageURL.isEmpty()){
        Picasso.get().load(Constant.IMAGE_BASE_URL+ imageURL).into(image);
        }else {
            image.setImageResource(R.drawable.no_image);
        }

        }
}
